<p style="font-size:12px">Bukit Serelo atau yang lebih dikenal dengan Gunung Jempol atau ada juga yang memberi nama Bukit Telunjuk adalah sebuah landmark yang dimiliki oleh wilayah Seganti Setungguan daerah kabupaten Lahat yang terletak di Desa Perangai. Seperti namanya lainnya yaitu Gunung Jempol, Bukit Serelo ini memiliki bentuk yang mirip seperti sebuah jempol dari tangan manusia yang menunjuk kearah atas. Disekitar bukit yang merupakan salah satu bagian dari bukit barisan ini dikelilingi oleh sungai lematang yang jernih sehingga membuat pemandangan disekitar bukit semakin mempesona. Bagian bawah bukit inipun kerap dijadikan tempat berkemah ataupun untuk rekreasi.</p>

