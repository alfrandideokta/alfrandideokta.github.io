<p style="font-size:12px">Untuk menikmati panorama yang indah dari atas bukit ini bukanlah merupakan suatu perkara yang mudah, hal ini dikarenakan selain jarak pendakiannya yang cukup jauh, licin serta terjal dimana sudut pendakiannya bisa mencapai hingga 75 derajat. Selain itu Bukit Serelo merupakan bukit bebatuan yang hanya ditumbuhi oleh semak-semak mirip seperti ilalang. Di sekitar lereng bukit ini terdapat sumber mata air jernih (185 m). Selain itu juga terdapat sebuah pemandangan yang indah berupa koleksi bebatuan grfitti yang halus (540 m) yang terletak dibagian bawah tebing puncak tak jauh dari Simpang Kedaton (410m) yang merupakan persimpangan penting diatas bukit ini. Sebagai catatan butuh ekstra kehati-hatian jika ingin camping diatas bukit ini, karena angin yang menerpa bukit ini cukup kuat sehingga mampu menerbangkan tenda, belum lagi disertai hawa yang cukup dingin.</p>
