<p style="font-size:12px"><b>SELAMAT datang di Lahat.</b> Sebuah kabupaten yang masih berada di Provinsi Sumatera Selatan ini memang tidak seterkenal Palembang, namun siapa sangka jika daerah yang dipimpin oleh Aswari Rivai tersebut memiliki alam yang sungguh menantang untuk dijelajahi.</p>


<p style="font-size:12px">Bagi traveller yang memiliki nyali besar dan ingin menguji nyali, bisa menyambangi Kabupaten Lahat. Pasalnya, pada kali pertama menjejakkan kaki di Kabupaten Lahat wisatawan pasti akan membelalakan mata. Barisan bukit-bukit, kelokan sungai berarus deras, hamparan hutan tropis diselingi ladang kopi dan jurang-jurang terjal akan mengisi pemandangan mata Anda.
Salah satu bukit yang perlu dijelajahi ketika berada di Lahat, ialah Bukit Serelo. Bukit unik berbentuk seperti jari jempol manusia tersebut merupakan salah satu bagian dari Taman Wisata Alam (TWA) Bukit Serelo yang juga bagian dari tiga kawasan konservasi di Kabupaten Lahat.</p>

<p style="font-size:12px">

Untuk mencapai lokasi TWA Bukit Serelo yang berada di Kecamatan Merapi, cukup mudah, sebab jalan sudah diaspal. Namun, untuk mencapai kaki bukit wisatawan harus meniti jalan setapak dengan pemandangan hutan lebat.

Nah, bagi wisatawan dan pecinta alam yang ingin mencapai puncak bukit atau berada di atas jempol, mereka harus mendaki bukit, memanjat tebing, yang tentunya bisa melatih nyali, karena bukit tersusun atas batuan jenis andesit. 
Tapi, tidak perlu risau, karena Bukit Serelo telah dibuat beberapa jalur pemanjatan oleh para pemanjat tebing, penggiat alam bebas, jadi memudahkan pemanjatan. Tapi, wisatawan masih perlu berhati-hati karena kemungkinan terpeleset atau jatuh masih bisa terjadi, karena terjalnya medan menuju puncak.
